package com.retailer.user11.onetonineapp.model;

public class WeightItem {
    private String get_date;
    private String get_weight;
    private String get_difference;
    private String week;

    public String getGet_date() {
        return get_date;
    }

    public void setGet_date(String get_date) {
        this.get_date = get_date;
    }

    public String getGet_weight() {
        return get_weight;
    }

    public void setGet_weight(String get_weight) {
        this.get_weight = get_weight;
    }

    public String getGet_difference() {
        return get_difference;
    }

    public void setGet_difference(String get_difference) {
        this.get_difference = get_difference;
    }

    public String getWeek() {
        return week;
    }

    public void setWeek(String week) {
        this.week = week;
    }
}
